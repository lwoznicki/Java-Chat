    private void jButtonToLogout(java.awt.event.ActionEvent evt) {
        String logoutInfo = "# " + nickname + " wylogowal sie. #";
        byte byteBufferLogoutInfo[] = logoutInfo.getBytes();
        try {
            InetAddress group = InetAddress.getByName("230.0.0.2");
            DatagramPacket packet = new DatagramPacket(byteBufferLogoutInfo, byteBufferLogoutInfo.length, group, 4446);
            datagramSocket.send(packet);
        } catch (Exception e) {
            System.err.println("Error while working with Button2\n" + e);
        }
        logoutInfo = "OFFLINE";
        byteBufferLogoutInfo = logoutInfo.getBytes();
        try {
            InetAddress group = InetAddress.getByName("230.0.0.2");
            DatagramPacket packet = new DatagramPacket(byteBufferLogoutInfo, byteBufferLogoutInfo.length, group, 5000);
            datagramSocket.send(packet);
            multicastSocket.leaveGroup(address);
            datagramSocket.close();
        } catch (Exception e) {
            System.err.println("Error has occured!" + e);
        }
        this.setVisible(false);
        new ChatApp().setVisible(true);
    }       

    private void jButtonToSend(java.awt.event.ActionEvent evt) {
        String textInMessage = jTextAreaMessage.getText();
        if (!textInMessage.equals("")) {
            message = "#" + nickname + ": " + textInMessage;
            try {
                byte[] bufferBytes;
                bufferBytes = message.getBytes();
                InetAddress group = InetAddress.getByName("230.0.0.2");
                DatagramPacket packet = new DatagramPacket(bufferBytes, bufferBytes.length, group, 4446);
                datagramSocket.send(packet);
            } catch (IOException e) {
                System.err.println(e);
                MulticastClient.multicastSocket.close();
            }
        }
    }               

